"""Построение курсовой работы через WorkBuilder.

Демонстрирует базовый fluent-API и сохранение в .docx.

Запуск:

    python examples/build_coursework.py [output.docx]
"""
from __future__ import annotations

import sys
from pathlib import Path

from gostforge.builder import work


def main() -> int:
    output = Path(sys.argv[1] if len(sys.argv) > 1 else "coursework.docx")

    (
        work(
            "Применение нормоконтроля в учебном процессе",
            author="Иванов И. И.",
            supervisor="Петров П. П.",
            organization="МГУ им. М. В. Ломоносова",
            year=2026,
            work_type="coursework",
        )
        .section("Введение")
        .paragraph(
            "Актуальность темы курсовой работы заключается в необходимости "
            "автоматизации проверки оформления студенческих работ по "
            "требованиям ГОСТ 7.32-2017 и кафедральных методичек."
        )
        .paragraph(
            "Цель работы — разработка инструмента, способного парсить .docx, "
            "проверять оформление и предлагать автоматические исправления."
        )
        .section("Глава 1. Анализ предметной области")
        .subsection("1.1 Обзор существующих решений")
        .paragraph("В настоящее время на рынке существует несколько систем нормоконтроля.")
        .subsection("1.2 Постановка задачи")
        .paragraph("Сформулируем задачу: автоматизировать проверку 30+ параметров оформления.")
        .section("Заключение")
        .paragraph("В ходе работы был разработан и протестирован инструмент gostforge.")
        .section("Список использованных источников")
        .reference(
            "ГОСТ 7.32-2017. Отчёт о научно-исследовательской работе. "
            "Структура и правила оформления. — М. : Стандартинформ, 2017. — 32 с."
        )
        .reference(
            "Иванов И. И. Системы автоматизированного нормоконтроля : "
            "учеб. пособие. — М. : Наука, 2024. — 320 с."
        )
        .save(output)
    )
    print(f"Создан файл: {output}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
