"""Конструктор: вкладка «Содержимое» — редактор активного раздела.

Здесь пользователь добавляет блоки (параграф, таблица, рисунок,
формула, список), редактирует run-ы внутри параграфов
(жирный/курсив/inline-формула/cross-ref/цитата), управляет
подразделами 2-3 уровней.

Активный раздел выбирается на странице «Структура» (через
``state["active_section_index"]``), но для быстрого переключения
здесь же показываем dropdown.
"""

from __future__ import annotations

import streamlit as st

from gostforge.web.pages.builder import _common_setup, _common_sidebar


def page() -> None:
    _common_setup()
    _common_sidebar()

    from gostforge.web.builder_editor import (
        _get_state,
        _render_active_section_editor,
    )

    st.title("Содержимое раздела")
    state = _get_state()
    sections = state.get("sections") or []
    if not sections:
        st.info(
            "Сначала создайте хотя бы один раздел на странице «Структура» — "
            "добавьте кнопкой «+ Раздел» или вставьте готовый шаблон."
        )
        return

    # Быстрый селектор активного раздела — для случая, когда пользователь
    # пришёл на эту страницу прямо по URL (минуя «Структуру»).
    idx = int(state.get("active_section_index", 0) or 0)
    if idx < 0 or idx >= len(sections):
        idx = 0
    options = list(range(len(sections)))
    new_idx = st.selectbox(
        "Активный раздел",
        options=options,
        index=options.index(idx),
        format_func=lambda i: f"{i + 1}. {sections[i].get('heading') or '(без названия)'}",
        key="content_active_section",
        help="Переключайте раздел, не переходя обратно на «Структуру».",
    )
    if new_idx != idx:
        state["active_section_index"] = int(new_idx)
        st.rerun()

    # Breadcrumb текущей позиции — пользователь сразу видит, где он
    # в иерархии работы (особенно полезно когда разделов 20+).
    active = sections[new_idx]
    heading = active.get("heading") or "(без названия)"
    n_subs = len(active.get("subsections") or [])
    n_blocks = len(active.get("blocks") or [])
    st.caption(
        f"📍 Раздел **{new_idx + 1} / {len(sections)}**: "
        f"«{heading}» · блоков {n_blocks} · подразделов {n_subs}"
    )

    # Help-блок: пользователь сразу видит весь доступный функционал
    # (новички не знают, что многое скрыто за кнопками блоков).
    # Свёрнут по умолчанию, чтобы не перегружать вид опытным.
    with st.expander("ℹ️ Что доступно на этой странице", expanded=False):
        st.markdown(
            """
**Внутри раздела:**

- **Название** — переименовать раздел.
- **Нормоконтроль раздела** — отключить отдельные проверки или все
  (для титульного / реферата / приложений).
- **Блоки**: добавить параграф, таблицу, рисунок, список, формулу
  или оглавление (кнопки `+ Параграф`, `+ Таблица`, … ниже).
- **Управление блоком**: `↑` / `↓` — переместить, `⎘` — дублировать,
  «Удалить блок» — удалить, «Перенести блок в раздел» — в другой раздел.
- **Шаблоны блоков** — вставить готовый сложный блок (таблица 3×3
  с подписью, рисунок с подписью и т. п.).

**Внутри параграфа** (inline-редактор):

- **+ Текст** — добавить TextRun (можно сделать жирным / курсивом /
  подчёркнутым / индексом).
- **+ Формула** — inline-формула в LaTeX.
- **+ Ссылка** — перекрёстная ссылка на рисунок / таблицу / формулу
  из этого же документа.
- **+ Цитата** — ссылка на источник библиографии (с опц. указанием
  страниц).

**Подразделы** (2–3 уровня) — у каждого свои блоки, своё название.

**Превью раздела в PDF** — внизу: компилирует только активный раздел
для быстрого визуального контроля.

> Чтобы добавить новый раздел или переставить разделы — откройте
> вкладку «Структура».
"""
        )

    _render_active_section_editor()
