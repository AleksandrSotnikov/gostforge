"""Конструктор: вкладка «Проверка» — live-нормоконтроль и импортированные сообщения.

Показывает:

* нарушения live-нормоконтроля для текущего state (сводка + детали с
  кликом «→ К разделу»);
* нарушения, найденные в .docx при импорте (если работа была загружена);
* комментарии рецензента из импортированного .docx.
"""

from __future__ import annotations

import streamlit as st

from gostforge.web.pages.builder import _common_setup, _common_sidebar


def page() -> None:
    _common_setup()
    _common_sidebar()

    from gostforge.web.builder_editor import (
        _render_import_violations_panel,
        _render_imported_comments_panel,
        _render_live_validation_panel,
    )

    st.title("Проверка")
    st.caption(
        "Live-нормоконтроль гоняется автоматически после каждой правки. "
        "Кнопка «→ К разделу» рядом с нарушением переключает вас на нужный "
        "раздел во вкладке «Содержимое»."
    )

    # Help-блок: единый стиль с другими подстраницами Конструктора.
    with st.expander("ℹ️ Что доступно на этой странице", expanded=False):
        st.markdown(
            """
**Live-нормоконтроль** — постоянная проверка текущего state по выбранному
профилю. Сводка: сколько найдено ошибок / предупреждений / инфо.
Раскройте expander — увидите детальный список:

- **код проверки** (например, `F.01`, `T.08`, `R.04`) — наведите
  курсор для расшифровки;
- **сообщение** — конкретная причина нарушения;
- **подсказка** — что исправить;
- **«→ К разделу»** — открыть проблемный раздел в «Содержимом».

**Если загружали .docx** через «Структура → Загрузить готовую работу»:

- **«Импортированные нарушения»** — что нашлось в исходном файле
  (до правок в конструкторе).
- **«Комментарии рецензента»** — Word-комментарии из исходного .docx.

> Чтобы автоматически починить часть нарушений — на «Экспорт»
> поставьте галочку «Применить автофиксы перед скачиванием».
> Чтобы вручную исключить проверку для одного раздела — на
> «Содержимое», expander «Нормоконтроль раздела».
"""
        )

    _render_import_violations_panel()
    _render_imported_comments_panel()
    _render_live_validation_panel()
