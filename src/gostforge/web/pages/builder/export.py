"""Конструктор: вкладка «Экспорт» — генерация финального .docx и других форматов.

`_render_generate_button` отвечает за:

* сборку state → Document → .docx через builder;
* PDF-превью через LibreOffice;
* экспорт в Markdown / HTML;
* опциональное применение автофиксов перед сборкой.
"""

from __future__ import annotations

import streamlit as st

from gostforge.web.pages.builder import _common_setup, _common_sidebar


def page() -> None:
    _common_setup()
    _common_sidebar()

    from gostforge.web.builder_editor import _render_generate_button

    st.title("Экспорт")
    st.caption(
        "Когда работа собрана и проверена — сгенерируйте файл в нужном формате. "
        "Перед экспортом можно применить автофиксы — они исправят безопасные "
        "ошибки оформления (двойные пробелы, прямые кавычки, кегль и т.д.)."
    )

    # Help-блок: пользователь сразу видит палитру форматов.
    with st.expander("ℹ️ Что доступно на этой странице", expanded=False):
        st.markdown(
            """
**Кнопки генерации:**

- **«Сгенерировать .docx»** — собрать финальный документ. Появится
  кнопка «Скачать .docx».
- **«Сгенерировать + превью PDF»** — то же + конвертация в PDF
  через LibreOffice (если установлен). Показывает превью прямо
  в браузере.

**Опция «Применить автофиксы перед скачиванием»** (по умолчанию
включена) — прогоняет автофиксеры по state перед сборкой:

- T.08 двойные пробелы, T.09 хвостовые пробелы;
- T.10 прямые кавычки `"…"` → ёлочки `«…»`;
- T.11 дефис между пробелами → длинное тире;
- T.12 / U.01 неразрывный пробел перед единицей измерения;
- T.14 интервалы между абзацами → 0 pt;
- H.01/H.02 формат заголовков (шрифт/кегль/жирность/цвет);
- F.01/F.02/F.03 поля и формат страницы → профиль;
- L.04 пунктуация в списках; … всего 28 автофиксеров.

**После генерации** доступны дополнительно:

- **Скачать .docx** (primary) — основной выход.
- **Скачать .pdf** — финальный PDF.
- **Скачать .md** — Markdown (для git-ревью / Obsidian).
- **Скачать .html** — standalone HTML с CSS для печати.

> Имя файла берётся из «Название работы» (sidebar). Если не задано —
> экспорт не запустится, пока не заполните.
"""
        )

    _render_generate_button()
