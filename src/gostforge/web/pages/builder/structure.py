"""Конструктор: вкладка «Структура» — дерево разделов и шаблоны.

Показывает прогресс работы, чек-лист готовности, оглавление-превью,
дерево разделов с действиями (↑↓ / дублировать / удалить), кнопки
вставки шаблонов разделов (Введение/Заключение/Приложение/...) и
bulk-операции.
"""

from __future__ import annotations

import streamlit as st

from gostforge.web.pages.builder import _common_setup, _common_sidebar


def page() -> None:
    _common_setup()
    _common_sidebar()

    from gostforge.web.builder_editor import (
        _render_bulk_operations_sidebar,
        _render_progress_panel,
        _render_readiness_panel,
        _render_section_templates_sidebar,
        _render_section_tree,
        _render_toc_preview_panel,
    )

    st.title("Структура работы")
    st.caption(
        "Сборка скелета: добавляйте разделы и подразделы, расставляйте порядок. "
        "Шаблоны разделов (Введение / Заключение / Приложение) — в sidebar. "
        "Когда структура готова, переходите к «Содержимое» для наполнения."
    )

    # Help-блок: новички сразу видят, где какие операции.
    with st.expander("ℹ️ Что доступно на этой странице", expanded=False):
        st.markdown(
            """
**Управление структурой:**

- **Дерево разделов** (кнопки `↑` / `↓` / `⎘` / `✕`) — переместить
  раздел, дублировать, удалить. Активный раздел подсвечен.
- **+ Раздел** / **+ Список литературы** — добавить пустой раздел
  в конец.
- **Перейти к разделу** — открыть выбранный раздел в редакторе
  «Содержимое».

**Шаблоны** (sidebar):

- **Шаблоны разделов** — Введение, Заключение, Реферат, Содержание,
  Список источников, Приложение, Глава, Титульный лист (вручную).
- **«Собрать каркас по ГОСТ»** — весь обязательный набор разделов
  в один клик.

**Bulk-операции** (sidebar):

- Удалить пустые параграфы во всех разделах.
- Заголовки в Title Case.
- Авто-нумерация глав («1 Анализ», «1.1 Подраздел», «1.1.1 Пункт»).
- Сбросить отключения проверок (skip-checks).
- Найти и заменить по всему документу.

**Сверху страницы:**

- **«Прогресс работы»** — метрики наполненности (параграфы,
  таблицы, рисунки, источники, слова).
- **«Готовность работы»** — чек-лист обязательных структурных
  элементов (титульник / содержание / введение / основная часть /
  заключение / список источников / приложения).
- **«Оглавление (preview)»** — список заголовков с глубинной
  отбивкой, как реальное оглавление.

> Наполнение разделов содержимым — на вкладке «Содержимое».
> Проверка — на вкладке «Проверка». Экспорт — на «Экспорт».
"""
        )

    _render_progress_panel()
    _render_readiness_panel()
    _render_toc_preview_panel()
    _render_section_tree()
    # Шаблоны разделов и bulk-операции живут в sidebar по историческим
    # причинам — функции сами добавляют свои секции через st.sidebar.
    _render_section_templates_sidebar()
    _render_bulk_operations_sidebar()
